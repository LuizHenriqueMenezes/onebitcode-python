### Antecessor e Sucessor de um número
# Escreva um programa em Python que leia um número e represente o número antecessor e 
# sucessor desse número que foi lido, utilizando operadores de atribuição.

num = int(input("Write a number\n"))

print(f'before the {num}: {num - 1}\nafter the {num}: {num + 1}\n')

### Média de 4 notas
# Escreva um programa em Python que leia quatro números e calcule a média entre esses números

note1 = float(input("Write the first note\n"))
note2 = float(input("Write the second note\n"))
note3 = float(input("Write the third note\n"))
note4 = float(input("Write the fourth note\n"))

print(f"average: {(note1 + note2 + note3 + note4) / 4:.2f}")

